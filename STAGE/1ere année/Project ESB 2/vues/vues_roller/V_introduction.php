                <h4><strong>Moyens à disposition</strong> <br> <br>
                    La Section s’est montée autour d’un lieu, la cour du collège Pierre de Ronsard, mise à disposition des adhérents via une convention signée avec l’établissement.<br>
                    Elle permet de pratiquer en plein air, sur un revêtement de qualité, à chaque fois que la météo est favorable.<br>
                    En complément et depuis plusieurs saisons, un accord avec la Section Badminton permet l’accès à une partie du gymnase de la Villatte, autorisant une poursuite de la pratique en toute sécurité y compris par mauvais temps ou encore par grand froid au cœur de l’hiver.<br>
                    Un accès ponctuel à la patinoire de BOURGUEIL (2 séances par an) est aussi possible selon la disponibilité de celle-ci et sur accord de la Mairie.<br>
                    La Section est équipée de petit matériel pédagogique (plots, jalons, ballons) afin de proposer des activités variées ainsi que d’un peu de matériel de prêt (rollers, casques et protections,) pour pouvoir faciliter les essais et dépanner les plus étourdis !<br>
                    L’animation est assurée par des bénévoles, eux-mêmes pratiquants.<br> </h4>
            </div>
        </div>
        <div class="col">
            <div class="text-square">
                <h2></h2>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4 class="h4i"><strong>Objectifs</strong> <br> <br>
                    L’objectif premier est de parvenir à une certaine autonomie de tous les adhérents que ce soit dans l’équipement (rollers, casque et protections), la mobilité (marche avant, slalom, changement de rythme, marche arrière, petits sauts) ou encore la gestion d’une situation d’urgence (évitement, freinage, gestion d’une chute éventuelle). <br>
                    L’apprentissage se veut ludique en profitant de toutes les situations de jeux pour progresser dans sa pratique.
                    Un des objectifs de la saison, une fois les beaux jours revenus, est de pouvoir accompagner l’ensemble du groupe lors d’une ou plusieurs petites randonnées en profitant de parcours à la fois accessibles et relativement sécurisés (piste cyclage, route partagée).
                </h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
           
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4><strong>Horaires et lieux des séances</strong> <br> <br>
                    Les séances se tiennent le dimanche matin, sur le créneau de 10h30 à 12h.
                    Le lieu de Rendez-Vous habituel est la cour du collège Pierre de Ronsard en cas de météo favorable, le gymnase de la Villatte prenant le relai dans le cas contraire.<br> </h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4><strong>Chiffres clés</strong> <br> <br>Nombre d’adhérents 2022/2023 : 56 (46 pratiquants et 10 parents accompagnateurs venant de 8 communes des départements 37 et 49)<br>
                    Record du nombre d’adhérents : 56<br>
                    Age des pratiquants en début de saison 2022/2023 : de 5 à 49ans<br> </h4>

