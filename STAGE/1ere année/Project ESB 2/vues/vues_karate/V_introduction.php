                <h4 class="h4i">
                    Son histoire remonte à la nuit des temps. Le KARATE est un art de combat à mains nues 
                    qui permet, grâce à un entraînement rationnel, d’éduquer le corps et l’esprit afin de trouver une réponse appropriée à toute forme d’agression.</h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4>Le pratiquant dispose d’un éventail très large de techniques, à la fois défensives et offensives,
                    basées sur l’utilisation des armes naturelles du corps humain (pieds, poings, coudes, genoux, etc.).</h4>
                <h4>  ‎ ‎ ‎ ‎  </h4>
                <h4>Cette technique de SELF-DEFENSE est extrêmement efficace et a été adoptée par la plupart des armées et polices du monde.</h4>
