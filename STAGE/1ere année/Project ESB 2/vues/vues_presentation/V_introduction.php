                <h3>L'entente sportive de Bourgueil est un club omnisport créé en 1956 représentant 14 discipline sur le bassin de vie de Bourgueil.</h3>
                <h3>Nos 14 section sportive comptent plus de 1000 adhérents pour 130 bénevoles, encadrant et éducateur sportifs ainsi que 5 salarié dont une secrétaire administrative.</h3>
