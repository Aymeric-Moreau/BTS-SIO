
    <h2>Le Jardin de la France</h2>
    <p>Tours, capitale du jardin de la France, a toujours su témoigner un attachement particulier à ses parcs et jardins
        qui totalisent
        près de 630 hectares de verdure. Jardins historiques, parcs botaniques, promenades et marchés aux fleurs font de
        Tours une ville reconnue
        pour la qualité de son environnement.
        <br>Le jardin botanique, le jardin (historique) des Prébendes d'Oé, le jardin du Musée des Beaux Arts, le parc
        Mirabeau, la plaine de la Gloriette,
        le parc de la cousinerie, contribuent ainsi à l'agrément et aux loisirs de la vie tourangelle.
        <br>La Ville célèbre enfin chaque année ce formidable patrimoine au travers d'une manifestation populaire et
        insolite : la Journée verte.</p>