   <h2>Le Cloître de la Psalette</h2>
    <p><img src="img/psalette.jpg" alt="La Psalette" width="324" height="133" ></p>
    <p>C'était le cloître des chanoines de la cathédrale, contre laquelle il est appuyé.
        Son aile ouest, dont il ne subsiste qu'une partie, et le retour de l'aile nord ont été édifiés vers 1460.
        L'aile ouest est formée de sept travées, ouvertes au rez-de-chaussée d'une arcade de la galerie et, à l'étage,
        d'une fenêtre carrée.
        Les arcades sont richement décorées (choux, vigne, chardons, fleurs).
        Tout l'étage abritait la bibliothèque des chanoines. Les deux galeries du nord et de l'est ont été ajoutées au
        début du XVI<sup>ème</sup> siècle.
        <br />A la rencontre de ces deux ailes fut édifiée, en 1524, une gracieuse tour d'escalier à jours à la façon du
        célèbre escalier du château de Blois.
        On y accède par une belle porte encadrée de pilastres à arabesques. Derrière l'escalier se trouvait la chapelle
        canoniale,
        dont les murs portent encore des vestiges de fresques (Rois mages, fuite en Egypte.).</p>

