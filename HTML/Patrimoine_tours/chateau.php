            <div class="paragrafe">La première résidence seigneuriale, partiellement en bois, semble avoir été élevée à l'époque carolingienne
            (avant 886), par <em>Hugues l'Abbé.</em> 
            Cet édifice fut remplacé au milieu du XIème siècle par un logis plus important, en pierre, appuyé
            contre le rempart gallo-romain.
            Il était composé d'une vaste salle (28m sur 8) et d'une tour carrée (6m de côté).
            Ces éléments ont été révélés par les fouilles effectuées de 1974 à 1978 par H. Galinié.
            L'édifice est à attribuer à la Maison d'Anjou (Geoffroy Martel), qui prit possession de la Touraine en
            1044 et y remplaça Thibault de Blois.
            La tour de Guise et la tour cylindrique dite du <strong>Cachot Secret</strong>  flanquaient les angles ouest du château,
            reconstruit légèrement à l'est du précédent
            A la fin du XIIIème siècle, sur la forme d'un quadrilatère cantonné de quatre puissantes tours.
            L'ouvrage était lui-mème entouré d'un fossé et d'un autre mur qui délimitait une basse cour. La tour de Guise
            servait de donjon.
            Son toit conique reposant sur un couronnement, refait au XVème siècle, a été récemment restauré.
            Son nom rappelle la captivité du jeune duc de Guise, qui s'en échappa en 1591.
            </div> 
