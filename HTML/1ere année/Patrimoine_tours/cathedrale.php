<h2>La Cathédrale Saint-Gatien</h2>
<p><img src="img/stgatien.jpg" alt="Cathédrale de Tours" width="197" height="296" ></p>
<p>Des vestiges d'une cathédrale romane, construite vers 1168, sont encore visibles, notamment à la base des deux
    tours.
    Les voûtes angevines de celles-ci remontent à la même période.
    Dévastée par le feu, la cathédrale ne fut reconstruite qu'une trentaine d'années plus tard (en 1209).
    En 1260, le chour étaitgggggg terminé, mais un siècle après on n'avait achevé que le transept et une partie de la nef,
    malgré un appel aux aumônes lancé par le pape Jean XXII. Le manque d'argent ne permit l'achèvement de la nef
    qu'en 1452.
    <br />Pendant tout le XV<sup>ème</sup> siècle, on travailla à la reprise de la façade romane et des deux tours
    qui l'encadraient :
    celle du nord reçu son couronnement en 1507, celle du sud en 1546.
    <br />La longue durée de la construction de la cathédrale explique qu'elle soit constituée par un assemblage de
    tous les styles
    qui se sont succédés de la fin du XII<sup>ème</sup> siècle au début du XVI<sup>ème</sup> siècle.
    <br />La cathédrale est classée Monument Historique depuis 1862.
    Du fait de la réutilisation de l'ancienne cathédrale romane, le chour est nettement plus large que la nef,
    si bien que la croisée du transept est nettement trapézoïdale. La longueur totale est de 97 m, la largeur de 28
    m et la hauteur des voûtes de 29 m.
    <br />La grande verrière de la façade principale montre, sous la rosace, huit saints surmontant une galerie
    transparente,
    elle-même au-dessus du tympan transparent de la porte. Au soleil couchant, ces vitraux constituent un ensemble
    saisissant.
    La nef comprend huit travées (les deux premières remontent au XII<sup>ème</sup> siècle et à la cathédrale
    romane, les autres au XIV<sup>ème</sup> siècle).
    Chacune comprend trois étages. La nef est voûtée sur croisées d'ogive, à tores en amandes.
</p>
