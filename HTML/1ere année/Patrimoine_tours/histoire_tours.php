
<header class="paragrafe">
            Tours, classée Ville d'art et d'histoire, témoigne d'un passé prestigieux,
            de l'empreinte d'époques et de styles qui ont laissé à la ville un patrimoine des plus riches et précieux.
            Capitale du royaume de France au XVème et XVIème siècle, la ville possède des trésors
            qu'elle a su préserver et promouvoir.
            Depuis 2000, Tours et plus généralement le Val de Loire font partie du <em> patrimoine mondial </em> de l'Unesco,
            au titre de paysage culturel.
